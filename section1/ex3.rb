## Learn Ruby the Hard Way
# Numbers and Math
# Exercise 3

# + plus does addition
# - minus does substraction
# / slash does division
# * asterisk does multiplication
# % modulus pulls remaining result from division of two integers or floats
# < less-than
# > greater-than
# <= less-than-equal
# >= greater-than-equal

# Prints a String
puts "I will now count my chickens:"

# Prints Hens followed by an In-String computation of Floats
puts "Hens #{25.0 + 30.0 / 6.0}"
# Prints Roosters followed by in-string computation of Floats
puts "Roosters #{100.0 - 25.0 * 3.0 % 4.0}"

# Prints a String
puts "Now I will count the eggs:"

# Prints computation of Floats
puts 3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0 / 4.0 + 6.0

# Prints a String
puts "Is it true that 3.0 + 2.0 < 5.0 - 7.0?"

# Prints a Boolean comparison of two computational Floats
puts 3.0 + 2.0 < 5.0 - 7.0

# Prints a String followed by an In-String computation of Floats
puts "What is 3.0 + 2.0? #{3.0 + 2.0}"
# Prints a String followed by an In-String computation of Floats
puts "What is 5.0 - 7.0? #{5.0 - 7.0}"

# Prints a String
puts "Oh, that's why it's false."

# Prints a String
puts "How about some more."

# Prints a String followed by a Boolean comparison of Floats
puts "Is it greater? #{5.0 > -2.0}"
# Prints a String followed by a Boolean comparison of Floats
puts "Is it greater or equal? #{5.0 >= -2.0}"
# Prints a String followed by a Boolean comparison of Floats
puts "Is it less or equal? #{5.0 <= -2.0}"
